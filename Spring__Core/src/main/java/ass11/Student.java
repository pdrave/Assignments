package ass11;

public class Student
{
	String studentname;
	int rollno;
	String dob;
	String classname;
	String section;
	Result obj;
	
	
	public Student(String studentname, int rollno, String dob, String classname, String section, Result obj) 
	{
		super();
		this.studentname = studentname;
		this.rollno = rollno;
		this.dob = dob;
		this.classname = classname;
		this.section = section;
		this.obj = obj;
	}
	
	
	

}
